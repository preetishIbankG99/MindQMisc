package package1;

public class StringOperation {

	public static void main(String[] args) {
	String str1="Selenium";
	String str2="Testing";
	System.out.println(str1+str2);
	//+concatenates SeleniumTesting
	System.out.println("Selenium"+1+1);//Selenium11
	System.out.println(1+1+"Selenium");//2Selenium
	System.out.println(1+1+6);//8 integer addition
	//String+String=concatenation /String
	//String+Integer=concatenation /String
	//Integer+Integer=Addition/Integer

	}

}
