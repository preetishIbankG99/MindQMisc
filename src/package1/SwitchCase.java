package package1;

public class SwitchCase {

	public static void main(String[] args) {
		int mark=90 ;
		switch(mark){
		case 80:
			System.out.println("Very Good");
			break;
		case 90:
			System.out.println("Fair");
			break;
		case 100:
			System.out.println("Excellent");
			break;
			
		default:	
			System.out.println("Invalid");
			break;

	}

}
}